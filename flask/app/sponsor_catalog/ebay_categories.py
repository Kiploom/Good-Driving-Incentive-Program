def get_all_categories():
    """Return a flat list of all categories with their full paths."""
    categories = []
    for parent_name, parent_data in EBAY_CATEGORIES.items():
        categories.append({
            "id": parent_data["id"],
            "name": parent_name,
            "parent": None,
            "full_path": parent_name
        })
        for sub_name, sub_id in parent_data.get("subcategories", {}).items():
            categories.append({
                "id": sub_id,
                "name": sub_name,
                "parent": parent_name,
                "full_path": f"{parent_name} > {sub_name}"
            })
    return categories


def get_category_tree():
    """Return the categories in a hierarchical tree structure."""
    return EBAY_CATEGORIES

